package javaClass_191203;

public class Ex03_Operator {

	public static void main(String[] args) {
		/*
		 * 정수형 변수 2개를 선언해서 
		 * +, -, *, /, % 연산을 각각 수행하고 결과를 출력하시오.  
		 */
		int number1, number2, result;
		number1 = 5;
		number2 = 3;
		result = number1 + number2;
		System.out.println("+결과 : " + result);
		result = number1 - number2;
		System.out.println("-결과 : " + result);
		result = number1 * number2;
		System.out.println("*결과 : " + result);
		result = number1 / number2;
		System.out.println("/결과 : " + result);
		result = number1 % number2;
		System.out.println("%결과 : " + result);
	}

}





